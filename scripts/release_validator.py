def render_markdown_summary(summary: ValidationSummary) -> str:
    """Render a concise Markdown triage report."""
    title_status = "Failed" if summary.errors else "Passed"
    lines = [
        f"## Release Validation: {title_status}",
        "",
        f"- Current output rules: {summary.counts.get('current_output_rules', 0):,}",
        f"- Errors: {len(summary.errors)}",
        f"- Warnings: {len(summary.warnings)}",
        "",
        "### Errors",
    ]

    if summary.errors:
        lines.extend(f"- `{item['code']}`: {item['message']}" for item in summary.errors)
    else:
        lines.append("- None")

    lines.append("")
    lines.append("### Warnings")
    if summary.warnings:
        lines.extend(f"- `{item['code']}`: {item['message']}" for item in summary.warnings)
    else:
        lines.append("- None")

    lines.append("")
    lines.append("### Thresholds")
    for key, value in summary.thresholds.items():
        lines.append(f"- `{key}`: {value}")

    lines.append("")
    return "\n".join(lines)


def save_validation_reports(
    summary: ValidationSummary,
    summary_json_path: Path,
    summary_md_path: Path,
) -> None:
    """Write JSON and Markdown validation summaries atomically."""
    _atomic_write_json(summary_json_path, summary.to_dict())
    _atomic_write_text(summary_md_path, render_markdown_summary(summary))


