def _add_blocked_domain(
    line: str,
    blocked_domains: set[str],
    wildcard_domains: set[str],
) -> None:
    """Record domain coverage represented by an emitted blocking line."""
    domain, _modifiers, is_exception, is_wildcard = extract_abp_info(line)
    if domain and not is_exception:
        target = wildcard_domains if is_wildcard else blocked_domains
        target.add(domain)
        return

    if line.startswith("@@"):
        return

    ip, hosts_domains = extract_hosts_info(line)
    if ip:
        for host_domain in hosts_domains:
            blocked_domains.add(normalize_domain(host_domain))
        return

    if PLAIN_DOMAIN_PATTERN.match(line):
        domain = normalize_domain(line)
        if domain and domain not in LOCAL_HOSTNAMES:
            blocked_domains.add(domain)


def scan_output(output_path: Path) -> OutputScan:
    """Scan emitted output for syntax errors and canary coverage indexes."""
    errors: list[Finding] = []
    blocked_domains: set[str] = set()
    wildcard_domains: set[str] = set()
    line_count = 0
    invalid_count = 0
    url_path_count = 0
    unsupported_modifier_count = 0
    exception_count = 0

    with open(output_path, encoding="utf-8-sig", errors="replace") as f:
        for line_number, raw_line in enumerate(f, start=1):
            line = raw_line.strip()
            if not line:
                continue

            line_count += 1
            syntax = classify_rule_syntax(line)
            if syntax.is_exception:
                exception_count += 1
                errors.append(_finding(
                    "output_exception_rule",
                    "Exception rules must not be emitted in the release artifact",
                    line_number=line_number,
                    rule=line,
                ))
            if syntax.is_invalid:
                invalid_count += 1
                errors.append(_finding(
                    "output_invalid_syntax",
                    "Invalid emitted rule syntax",
                    line_number=line_number,
                    rule=line,
                ))
            if syntax.has_url_path:
                url_path_count += 1
                errors.append(_finding(
                    "output_url_path",
                    "URL-path rule emitted in DNS blocklist output",
                    line_number=line_number,
                    rule=line,
                ))

            modifiers = set(syntax.modifier_names)
            if modifiers and has_unsupported_modifiers(modifiers):
                unsupported_modifier_count += 1
                errors.append(_finding(
                    "output_unsupported_modifier",
                    "Browser-only modifier emitted in DNS blocklist output",
                    line_number=line_number,
                    rule=line,
                    modifiers=sorted(modifiers),
                ))

            if not syntax.is_exception:
                _add_blocked_domain(line, blocked_domains, wildcard_domains)

    syntax_details = {
        "lines_scanned": line_count,
        "invalid_rules": invalid_count,
        "url_path_rules": url_path_count,
        "unsupported_modifier_rules": unsupported_modifier_count,
        "exception_rules": exception_count,
    }
    return OutputScan(
        line_count=line_count,
        errors=errors,
        blocked_domains=blocked_domains,
        wildcard_domains=wildcard_domains,
        syntax=syntax_details,
    )


def _domain_is_blocked(
    domain: str,
    blocked_domains: set[str],
    wildcard_domains: set[str],
) -> bool:
    """Return True when emitted domain or parent coverage blocks a domain."""
    normalized = normalize_domain(domain)
    if normalized in blocked_domains:
        return True

    for parent in walk_parent_domains(normalized):
        if parent in blocked_domains:
            return True

    return any(normalized.endswith(f".{wildcard_domain}") for wildcard_domain in wildcard_domains)


def validate_canaries(
    canaries: dict[str, object],
    blocked_domains: set[str],
    wildcard_domains: set[str],
) -> tuple[list[Finding], dict[str, object]]:
    """Validate must-block and must-allow canary domains."""
    errors: list[Finding] = []
    must_block = canaries.get("must_block")
    must_allow = canaries.get("must_allow")
    if not isinstance(must_block, list) or not isinstance(must_allow, list):
        return [
            _finding("canary_config_invalid", "Canary config must define domain lists")
        ], {}

    must_block_results: list[dict[str, object]] = []
    must_allow_results: list[dict[str, object]] = []
    for domain in must_block:
        domain_text = str(domain)
        blocked = _domain_is_blocked(domain_text, blocked_domains, wildcard_domains)
        must_block_results.append({"domain": domain_text, "blocked": blocked})
        if not blocked:
            errors.append(_finding(
                "canary_must_block_missing",
                "Must-block canary is not covered by emitted output",
                domain=domain_text,
            ))

    for domain in must_allow:
        domain_text = str(domain)
        blocked = _domain_is_blocked(domain_text, blocked_domains, wildcard_domains)
        must_allow_results.append({"domain": domain_text, "blocked": blocked})
        if blocked:
            errors.append(_finding(
                "canary_must_allow_blocked",
                "Must-allow canary is blocked by emitted output",
                domain=domain_text,
            ))

    return errors, {
        "must_block": must_block_results,
        "must_allow": must_allow_results,
    }


