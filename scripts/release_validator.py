def validate_release(
    *,
    source_health_path: Path,
    pipeline_stats_path: Path,
    output_path: Path,
    canaries_path: Path,
    previous_output_path: Path | None = None,
    summary_json_path: Path | None = None,
    summary_md_path: Path | None = None,
    evidence_json_path: Path | None = None,
    thresholds: ReleaseThresholds = DEFAULT_THRESHOLDS,
) -> ValidationSummary:
    """Validate a release candidate and optionally write summary reports."""
    source_health = _load_json(source_health_path, "source-health report")
    pipeline_stats, pipeline_stats_load_errors = _load_pipeline_stats(pipeline_stats_path)
    canaries = _load_json(canaries_path, "canary config")

    errors = _validate_report_schema_versions(
        source_health,
        pipeline_stats,
        canaries,
        validate_pipeline_stats=not pipeline_stats_load_errors,
    )
    errors.extend(pipeline_stats_load_errors)
    warnings: list[Finding] = []
    pipeline_count, pipeline_count_errors = _extract_pipeline_output_count(
        pipeline_stats,
        pipeline_stats_path,
    )
    errors.extend(pipeline_count_errors)

    source_errors, source_warnings, source_details = validate_source_health(
        source_health,
        thresholds,
    )
    errors.extend(source_errors)
    warnings.extend(source_warnings)

    output_scan = scan_output(output_path)
    errors.extend(output_scan.errors)

    current_count = output_scan.line_count
    if pipeline_count is not None and pipeline_count != current_count:
        errors.append(_finding(
            "pipeline_output_count_mismatch",
            "Pipeline reported output count does not match scanned release artifact",
            pipeline_reported_output_rules=pipeline_count,
            scanned_output_rules=current_count,
            absolute_delta=abs(pipeline_count - current_count),
            pipeline_stats_path=str(pipeline_stats_path),
            schema_version=pipeline_stats.get("schema_version"),
            field="statistics.lines_output",
        ))

    if current_count < thresholds.minimum_output_rules:
        errors.append(_finding(
            "output_below_minimum_count",
            "Current output rule count is below the hard minimum",
            current_count=current_count,
            minimum=thresholds.minimum_output_rules,
        ))

    canary_errors, canary_details = validate_canaries(
        canaries,
        output_scan.blocked_domains,
        output_scan.wildcard_domains,
        coverage_records=output_scan.coverage_records,
        canaries_path=canaries_path,
    )
    errors.extend(canary_errors)

    previous_release: dict[str, object] = {
        "available": False,
        "current_count": current_count,
        "previous_count": None,
        "absolute_delta": None,
        "relative_delta": None,
    }
    previous_rules: tuple[str, ...] | None = None
    if previous_output_path and previous_output_path.exists():
        previous_rules = _read_non_empty_lines(previous_output_path)
        previous_count = len(previous_rules)
        previous_summary = validate_previous_output_delta(current_count, previous_count, thresholds)
        errors.extend(previous_summary.errors)
        warnings.extend(previous_summary.warnings)
        previous_release = previous_summary.previous_release
    else:
        warnings.append(_finding(
            "previous_output_unavailable",
            "Previous release output is unavailable; delta comparison skipped",
        ))

    counts = {"current_output_rules": current_count}
    if pipeline_count is not None:
        counts["pipeline_reported_output_rules"] = pipeline_count

    release_evidence, evidence_sidecar = _release_evidence_payload(
        current_rules=output_scan.rules,
        previous_rules=previous_rules,
        source_health=source_health,
        scoped_canaries=canary_details.get("scoped", []),
        coverage_records=output_scan.coverage_records,
        evidence_json_path=evidence_json_path,
    )

    summary = ValidationSummary(
        errors=errors,
        warnings=warnings,
        generated_at=_utc_now(),
        thresholds=_thresholds_to_dict(thresholds),
        counts=counts,
        source_health=source_details,
        syntax=output_scan.syntax,
        canaries=canary_details,
        previous_release=previous_release,
        release_evidence=release_evidence,
    )

    if summary_json_path and summary_md_path:
        save_validation_reports(
            summary,
            summary_json_path,
            summary_md_path,
            evidence_json_path=evidence_json_path,
            evidence_sidecar=evidence_sidecar,
        )

    return summary


def run_validation(
    *,
    source_health_path: Path,
    pipeline_stats_path: Path,
    output_path: Path,
    canaries_path: Path,
    previous_output_path: Path | None,
    summary_json_path: Path,
    summary_md_path: Path,
    evidence_json_path: Path | None = None,
    thresholds: ReleaseThresholds = DEFAULT_THRESHOLDS,
) -> ValidationRunResult:
    """Validate and return a CLI-style exit code."""
    summary = validate_release(
        source_health_path=source_health_path,
        pipeline_stats_path=pipeline_stats_path,
        output_path=output_path,
        canaries_path=canaries_path,
        previous_output_path=previous_output_path,
        summary_json_path=summary_json_path,
        summary_md_path=summary_md_path,
        evidence_json_path=evidence_json_path,
        thresholds=thresholds,
    )
    return ValidationRunResult(summary=summary, exit_code=1 if summary.errors else 0)


