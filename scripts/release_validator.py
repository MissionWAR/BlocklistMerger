def _parse_args() -> argparse.Namespace:
    """Parse CLI arguments."""
    parser = argparse.ArgumentParser(
        prog="scripts.release_validator",
        description="Validate release candidate safety before publishing.",
    )
    parser.add_argument("--source-health", required=True, help="Path to source-health JSON")
    parser.add_argument("--pipeline-stats", required=True, help="Path to pipeline-stats JSON")
    parser.add_argument("--output", required=True, help="Path to current merged output")
    parser.add_argument("--canaries", required=True, help="Path to release canaries JSON")
    parser.add_argument("--previous-output", help="Optional previous release output")
    parser.add_argument("--summary-json", required=True, help="Path for validation JSON summary")
    parser.add_argument("--summary-md", required=True, help="Path for validation Markdown summary")
    parser.add_argument(
        "--minimum-output-rules",
        type=int,
        default=DEFAULT_MINIMUM_OUTPUT_RULES,
        help="Hard minimum emitted rule count",
    )
    return parser.parse_args()


def main() -> int:
    """CLI entry point."""
    args = _parse_args()
    previous_output = Path(args.previous_output) if args.previous_output else None
    thresholds = ReleaseThresholds(minimum_output_rules=args.minimum_output_rules)

    try:
        result = run_validation(
            source_health_path=Path(args.source_health),
            pipeline_stats_path=Path(args.pipeline_stats),
            output_path=Path(args.output),
            canaries_path=Path(args.canaries),
            previous_output_path=previous_output,
            summary_json_path=Path(args.summary_json),
            summary_md_path=Path(args.summary_md),
            thresholds=thresholds,
        )
        if result.exit_code:
            print(f"Release validation failed with {len(result.summary.errors)} error(s)")
        else:
            print(f"Release validation passed with {len(result.summary.warnings)} warning(s)")
        return result.exit_code
    except Exception as e:
        print(f"\nERROR: {e}", file=sys.stderr)
        import traceback

        traceback.print_exc()
        return 1


