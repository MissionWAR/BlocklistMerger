@dataclass(frozen=True, slots=True)
class ReleaseThresholds:
    """Numeric release validation thresholds."""

    minimum_output_rules: int = DEFAULT_MINIMUM_OUTPUT_RULES
    source_failed_stale_minimum: int = DEFAULT_SOURCE_FAILED_STALE_MINIMUM
    source_fallback_stale_minimum: int = DEFAULT_SOURCE_FALLBACK_STALE_MINIMUM
    source_hard_ratio: float = DEFAULT_SOURCE_HARD_RATIO
    previous_extreme_drop_ratio: float = DEFAULT_PREVIOUS_EXTREME_DROP_RATIO
    previous_extreme_increase_ratio: float = DEFAULT_PREVIOUS_EXTREME_INCREASE_RATIO
    previous_moderate_delta_ratio: float = DEFAULT_PREVIOUS_MODERATE_DELTA_RATIO
    previous_extreme_absolute_delta: int = DEFAULT_PREVIOUS_EXTREME_ABSOLUTE_DELTA


DEFAULT_THRESHOLDS: Final[ReleaseThresholds] = ReleaseThresholds()


@dataclass(slots=True)
class ValidationSummary:
    """Versioned validation summary data used for JSON and Markdown reports."""

    errors: list[Finding] = field(default_factory=list)
    warnings: list[Finding] = field(default_factory=list)
    schema_version: int = VALIDATION_SUMMARY_SCHEMA_VERSION
    version: str = __version__
    generated_at: str = ""
    thresholds: dict[str, object] = field(default_factory=dict)
    counts: dict[str, int] = field(default_factory=dict)
    source_health: dict[str, object] = field(default_factory=dict)
    syntax: dict[str, object] = field(default_factory=dict)
    canaries: dict[str, object] = field(default_factory=dict)
    previous_release: dict[str, object] = field(default_factory=dict)
    release_evidence: dict[str, object] = field(default_factory=dict)

    @property
    def status(self) -> str:
        """Return failed when any hard validation error exists."""
        return "failed" if self.errors else "passed"

    def to_dict(self) -> dict[str, object]:
        """Return the JSON-serializable summary shape."""
        return {
            "schema_version": self.schema_version,
            "version": self.version,
            "generated_at": self.generated_at,
            "status": self.status,
            "errors": self.errors,
            "warnings": self.warnings,
            "thresholds": self.thresholds,
            "counts": self.counts,
            "source_health": self.source_health,
            "syntax": self.syntax,
            "canaries": self.canaries,
            "previous_release": self.previous_release,
            "release_evidence": self.release_evidence,
        }


