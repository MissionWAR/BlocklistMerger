def _utc_now() -> str:
    """Return the current UTC timestamp in report format."""
    return time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())


def _finding(code: str, message: str, **details: object) -> Finding:
    """Build a stable finding object."""
    finding: Finding = {"code": code, "message": message}
    if details:
        finding["details"] = details
    return finding


def _thresholds_to_dict(thresholds: ReleaseThresholds) -> dict[str, object]:
    """Return thresholds as a JSON-compatible dictionary."""
    return {
        "minimum_output_rules": thresholds.minimum_output_rules,
        "source_failed_stale_minimum": thresholds.source_failed_stale_minimum,
        "source_fallback_stale_minimum": thresholds.source_fallback_stale_minimum,
        "source_hard_ratio": thresholds.source_hard_ratio,
        "previous_extreme_drop_ratio": thresholds.previous_extreme_drop_ratio,
        "previous_extreme_increase_ratio": thresholds.previous_extreme_increase_ratio,
        "previous_moderate_delta_ratio": thresholds.previous_moderate_delta_ratio,
        "previous_extreme_absolute_delta": thresholds.previous_extreme_absolute_delta,
    }


def _load_json(path: Path, description: str) -> dict[str, object]:
    """Load a JSON object from disk."""
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    if not isinstance(data, dict):
        msg = f"{description} must be a JSON object: {path}"
        raise ValueError(msg)
    return data


def _atomic_write_text(path: Path, text: str) -> None:
    """Write text through an atomic sibling temp file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    temp_path = path.with_suffix(".tmp")
    with open(temp_path, "w", encoding="utf-8", newline="\n") as f:
        f.write(text)
    temp_path.replace(path)


def _atomic_write_json(path: Path, data: dict[str, object]) -> None:
    """Write JSON through an atomic sibling temp file."""
    path.parent.mkdir(parents=True, exist_ok=True)
    temp_path = path.with_suffix(".tmp")
    with open(temp_path, "w", encoding="utf-8", newline="\n") as f:
        json.dump(data, f, indent=2, sort_keys=True)
        f.write("\n")
    temp_path.replace(path)


def _non_empty_line_count(path: Path) -> int:
    """Count non-empty lines in a rule output file."""
    with open(path, encoding="utf-8-sig", errors="replace") as f:
        return sum(1 for line in f if line.strip())


def _source_health_counts(report: dict[str, object]) -> tuple[int, dict[str, int]]:
    """Extract stable source-health totals from a report."""
    source_count = int(report.get("source_count") or 0)
    totals = report.get("totals_by_status")
    counts = {status: 0 for status in SOURCE_HEALTH_STATUSES}

    if isinstance(totals, dict):
        for status in SOURCE_HEALTH_STATUSES:
            counts[status] = int(totals.get(status, 0))

    if source_count <= 0:
        sources = report.get("sources")
        if isinstance(sources, list):
            source_count = len(sources)
            counts = {status: 0 for status in SOURCE_HEALTH_STATUSES}
            for source in sources:
                if isinstance(source, dict):
                    status = str(source.get("status", ""))
                    if status in counts:
                        counts[status] += 1

    return source_count, counts


def _source_hard_limit(total_sources: int, minimum: int, ratio: float) -> int:
    """Calculate a hard limit from source count, floor, and ratio."""
    return max(minimum, math.ceil(total_sources * ratio))


def _validate_report_schema_versions(
    source_health: dict[str, object],
    pipeline_stats: dict[str, object],
    canaries: dict[str, object],
) -> list[Finding]:
    """Return hard errors for unsupported report schema versions."""
    errors: list[Finding] = []
    if source_health.get("schema_version") != SOURCE_HEALTH_SCHEMA_VERSION:
        errors.append(_finding("source_health_schema_version", "Unsupported source-health schema"))
    if pipeline_stats.get("schema_version") != PIPELINE_STATS_SCHEMA_VERSION:
        errors.append(_finding(
            "pipeline_stats_schema_version",
            "Unsupported pipeline-stats schema",
        ))
    if canaries.get("schema_version") != 1:
        errors.append(_finding("canary_schema_version", "Unsupported canary schema"))
    return errors


# =============================================================================
# SOURCE HEALTH VALIDATION
# =============================================================================


def validate_source_health(
    report: dict[str, object],
    thresholds: ReleaseThresholds,
) -> tuple[list[Finding], list[Finding], dict[str, object]]:
    """Validate source-health report totals against two-tier release gates."""
    total_sources, counts = _source_health_counts(report)
    failed_stale = counts["failed"] + counts["stale_cache"]
    fallback_stale = counts["fallback_cache"] + counts["stale_cache"]
    failed_stale_limit = _source_hard_limit(
        total_sources,
        thresholds.source_failed_stale_minimum,
        thresholds.source_hard_ratio,
    )
    fallback_stale_limit = _source_hard_limit(
        total_sources,
        thresholds.source_fallback_stale_minimum,
        thresholds.source_hard_ratio,
    )

    errors: list[Finding] = []
    warnings: list[Finding] = []
    if failed_stale > failed_stale_limit:
        errors.append(_finding(
            "source_health_catastrophic_failed_stale",
            "Failed plus stale sources exceed the hard release threshold",
            failed_stale=failed_stale,
            limit=failed_stale_limit,
        ))
    if fallback_stale > fallback_stale_limit:
        errors.append(_finding(
            "source_health_catastrophic_fallback_stale",
            "Fallback plus stale sources exceed the hard release threshold",
            fallback_stale=fallback_stale,
            limit=fallback_stale_limit,
        ))

    degraded = counts["failed"] + counts["fallback_cache"] + counts["stale_cache"]
    if degraded and not errors:
        warnings.append(_finding(
            "source_health_degraded",
            "One or more sources used fallback cache, stale cache, or failed",
            degraded_sources=degraded,
        ))

    details = {
        "source_count": total_sources,
        "totals_by_status": counts,
        "failed_stale": failed_stale,
        "fallback_stale": fallback_stale,
        "failed_stale_hard_limit": failed_stale_limit,
        "fallback_stale_hard_limit": fallback_stale_limit,
    }
    return errors, warnings, details


